    ProveedorTo prov = new ProveedorTo();
    ProveedorController obj = new ProveedorController();

    private void buscar() {
        try {
            prov = obj.ProveedorBuscar(txtcod.getText());
            if (prov != null) {
                txtraz.setText(prov.getRazonsocial());
                txtdir.setText(prov.getDireccion() + "");
                txtruc.setText(prov.getRuc() + "");
                txtfono.setText(prov.getTelefono() + "");
            } else {
                JOptionPane.showMessageDialog(this, "Proveedor no existe");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void procesar(int i) {
        String msg = "";
        try {
            switch (i) {
                case 1:
                    leerDatos();
                    obj.ProveedorAdicionar(prov);
                    txtcod.setText(prov.getIdproveedor());
                    msg = "Proveedor registrado con exito";
                    limpiar();
                    break;
                case 2:
                    leerDatos();
                    obj.ProveedorActualizar(prov);
                    msg = "Proveedor actualizado con exito";
                    limpiar();
                    break;
                case 3:
                    leerDatos();
                    obj.ProveedorEliminar(prov);
                    msg = "Proveedor eliminado con exito";
                    limpiar();
                    break;
            }
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void leerDatos() {
         prov = new ProveedorTo();
        prov.setIdproveedor(txtcod.getText());
        prov.setRazonsocial(txtraz.getText());
        prov.setDireccion(txtdir.getText());
        prov.setRuc(txtruc.getText());
        prov.setTelefono(txtfono.getText());  
    }
    
    private void limpiar(){
        txtcod.setText("");
        txtraz.setText("");
        txtdir.setText("");
        txtruc.setText("");
        txtfono.setText("");
    }
